package Java核心_运算符;

public class OperatorDemo {
    public static void main(String[] args) {
        //定义两个int类型的变量
        int a = 5;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println("----------------");

        System.out.println(5/4);
        System.out.println(5.0/4);
        System.out.println(5/4.0);
    }
}
