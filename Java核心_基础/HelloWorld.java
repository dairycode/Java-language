package Java核心_基础;

/**
    这是我的第一个Java程序,HelloWorld案例
 */
public class HelloWorld {   //这里是在定义HelloWorld类,Java程序最小的单位是类
    /**
        main方法是程序的主入口,它的写法是固定的
     */
    public static void main(String[] args) {
        /*
            这个是main方法的主体,
            我们要实现的功能都要写到这里
         */
        //这是一个输出语句,可以将结果打印到控制台中
        System.out.println("Hello World");
    }
}